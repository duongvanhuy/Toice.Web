package BEAN;

public class SilderBanner {


	
	private int SildeBannerID;
	private String SildeBannerName;
	private String SildeBannerContent;
	private String	SildeImage;
	
	
	public int getSildeBannerID() {
		return SildeBannerID;
	}
	public void setSildeBannerID(int sildeBannerID) {
		SildeBannerID = sildeBannerID;
	}
	public String getSildeBannerName() {
		return SildeBannerName;
	}
	public void setSildeBannerName(String sildeBannerName) {
		SildeBannerName = sildeBannerName;
	}
	public String getSildeBannerContent() {
		return SildeBannerContent;
	}
	public void setSildeBannerContent(String sildeBannerContent) {
		SildeBannerContent = sildeBannerContent;
	}
	public String getSildeImage() {
		return SildeImage;
	}
	public void setSildeImage(String sildeImage) {
		SildeImage = sildeImage;
	}
	
	
	
}
